// for cria um intervalo

// valor inicial ; condição; soma um valor a mais;
for (var i = 0; i <= 100; i++) {
  console.log(i);
}

var j = 0;
while (j <= 100) {
  console.log(j);
  j++;
}
// quando usar for e quando usar while?
// for sempre que souber o tamanho do intervalo
// while é quando geralmente não sabemos quantas vezes o fluxo será executado
